"""Backend API — FastAPI application.

Endpoints:
  GET  /healthz | /livez      liveness (process up)
  GET  /readyz                readiness (manifest-only KB status — cheap)
  GET  /metrics               Prometheus exposition
  POST /api/v1/assessments    run an assessment on an uploaded snapshot
  GET  /api/v1/assessments    recent assessment summaries
  GET  /api/v1/assessments/{id}        full report JSON
  GET  /api/v1/assessments/{id}/html   rendered HTML report
  GET  /api/v1/assessments/{id}/markdown  markdown report
  GET  /                      web UI (bundled as package data)

Operational posture (multi-replica ready):
  - The reports directory is the source of truth; the in-memory map is a
    read cache. Listing and retrieval both fall back to disk, so replicas
    sharing a volume are stateless and interchangeable.
  - Load shedding (concurrency semaphore → 503) bounds parallel work;
    token-bucket rate limiting (→ 429) bounds request *rate* and thereby
    LLM spend; the idempotency cache collapses identical re-submissions.
  - Every request gets an X-Request-ID, bound into all structured logs.
"""

